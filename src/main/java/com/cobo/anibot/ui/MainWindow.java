    @Override
    public void botStarted(Bot bot) {
        botStatusLabel.setText("Bot status: Running");
        startBotButton.setText("Stop Bot");
    }
    
    @Override
    public void botUpdated(Bot bot) {
        if (bot.isRunning()) {
            botStatusLabel.setText("Bot status: Running");
        } else  {
            botStatusLabel.setText("Bot status: Not running");
        }
    }
    
    @Override
    public void botStoped(Bot bot) {
        botStatusLabel.setText("Bot status: Not running");
        startBotButton.setText("Start Bot");
        startBotButton.setEnabled(true);
    }

    private void startBotButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_startBotButtonMouseClicked
        if (botThread != null && botThread.isAlive()) {
            bot.setRunning(false);
            botStatusLabel.setText("Bot status: Stopping");
            startBotButton.setEnabled(false);
        } else {
            botThread = new Thread(bot);
            botThread.start();
        }
    }//GEN-LAST:event_startBotButtonMouseClicked

